#database란 여러 사람이 공유하여 사용할 목적으로 체계화해 통합, 관리하는 데이터의 집합체

#dbms란 사용자와 데이터베이스 사이에서 사용자의 요구에 따라 정보르 생성해주고 데이터베이스를 관리해주는 소프트웨어(ex, mysql)

#관계형 데이터스(RDB)란 서로간에 관계가 있는 데이터 테이블들으 모아둔 데이터 저장공간

#SQL이란 데이터베이스에서 데이터를 정의,조작,제어하기 위해 사용하는 언어

#데이터 넝의 언어(DDL) : CREATE, ALTER, DROP
#데이터 조작 언어(DML) : INSERT, UPDATE, DELETE, SELECT
#데이터 제어 언어(DCL) : GRANT, REVOKE, COMIT, ROLLBACK


